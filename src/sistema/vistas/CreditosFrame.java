    public void buscarClientes(String cliente){
        
        try {
            DefaultTableModel modelo;
            
            modelo = func.mostrar(cliente);

            tablaCreditos.setModel(modelo);
           
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }
    
    public void buscarDetalleCredito(){
        String criterio = String.valueOf(idCreditoSeleccionado);
        try {
            DefaultTableModel modelo= modelo = detalleCredito.mostrar(criterio);

            tablaDetalleCredito.setModel(modelo);
           
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Dcredito credito = new Dcredito();
        credito.setIdventa(Integer.parseInt(this.txtCodigoVenta.getText()));
        
         Calendar cal;
        int d,m,a;
        cal=dcfecha.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        credito.setFecha(new Date(a,m,d));
        
        credito.setMonto(Float.parseFloat(this.txtMontoCredito.getText()));
        
        Dcliente objCliente = (Dcliente) modeloCliente.getSelectedItem();
        credito.setIdcliente(objCliente.getId());
        
        if(func.insertar(credito)){
            JOptionPane.showMessageDialog(null, "El crédito ha sido guardado");
            tablaCreditos.setVisible(true);
            buscarClientes("");
        }else{
            JOptionPane.showMessageDialog(null, "No se pudo guardar");
        }
        
        this.tablaCreditos.setVisible(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

