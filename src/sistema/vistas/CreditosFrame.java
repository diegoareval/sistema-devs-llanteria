    private void btnPagarCuotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarCuotaActionPerformed
        // TODO add your handling code here:
         Calendar cal;
        int d,m,a;
        cal=fechaCuota.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        
        if(idCreditoSeleccionado==0){
            JOptionPane.showMessageDialog(null, "Seleccione un registro de crédito");
            return;
        }
        DdetalleCredito objDetalle = new DdetalleCredito();
        objDetalle.setIdcredito(idCreditoSeleccionado);
        objDetalle.setFecha(new Date(a,m,d));
        objDetalle.setPago(Double.parseDouble(txtPagoCuota.getText()));
        if(detalleCredito.insertar(objDetalle)){
            JOptionPane.showMessageDialog(null, "Se ha realizado el pago de credito");
            buscarDetalleCredito(); // funcion cargar detalle de credito
            calcularDeuda();
        }else{
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
        }
      
    }//GEN-LAST:event_btnPagarCuotaActionPerformed

