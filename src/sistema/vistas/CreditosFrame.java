    public void calcularDeuda(){
       int filas= tablaDetalleCredito.getRowCount();
       double montoCredito = Double.parseDouble(labelTotalCredito.getText());
       
       //variable de la sumatoria de la venta
            double suma=0;
            for (int i = 0; i < filas; i++) {
              String importe= (String)tablaDetalleCredito.getValueAt(i, 2);
              suma+=Double.parseDouble(importe) ;
              
            }
            suma= montoCredito-suma;
           labelMonto.setText(String.valueOf(suma));
           
           if(suma>0){
               labelMonto.setForeground(Color.red);
           }else{
               labelMonto.setForeground(Color.BLUE);
           }
    }
