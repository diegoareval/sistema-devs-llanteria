    private void cargarModeloCalidad(){
        limpiarTabla();
         ArrayList <Calidad> listaCalidad = base.obtenerCalidad("");
         
         int numeroFilas = listaCalidad.size();
         modeloTabla.setNumRows(numeroFilas);
         
         for (int i = 0; i < numeroFilas; i++) {
             Calidad calidad = listaCalidad.get(i);
             
             int clave =calidad.getIdCalidad();
             String nombre = calidad.getNombreCalidad();
             
             modeloTabla.setValueAt(clave, i, 0);
             modeloTabla.setValueAt(calidad, i, 1);
         }
         
     }
    
    private void limpiarTabla(){
        int numFila = modeloTabla.getRowCount(); // cantidad de filas
        if (numFila >0) {
            // debe de ser i mayor o igual a cero
            for (int i = numFila-1; i >=0; i--) { // recorre de mayor a menor
                modeloTabla.removeRow(i); // borra la fila
            }
        }
    }
