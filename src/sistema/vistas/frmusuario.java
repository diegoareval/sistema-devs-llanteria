    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion = "editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtid.setText(tablalistado.getValueAt(fila, 0).toString());
        txtusuario.setText(tablalistado.getValueAt(fila, 1).toString());
        txtcontra.setText(tablalistado.getValueAt(fila, 2).toString());
        cbotipo.setSelectedItem(tablalistado.getValueAt(fila, 3).toString());

    }//GEN-LAST:event_tablalistadoMouseClicked

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtid.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estás seguro de Eliminar el Usuario?", "Confirmar", 2);

            if (confirmacion == 0) {
                fusuario func = new fusuario();
                Dusuario dts = new Dusuario();

                dts.setId(Integer.parseInt(txtid.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }

    }//GEN-LAST:event_btneliminarActionPerformed

