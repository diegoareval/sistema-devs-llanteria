    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if (txtusuario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un usuario");
            txtusuario.requestFocus();
            return;
        }

        if (txtcontra.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la contraseña del usuario");
            txtcontra.requestFocus();
            return;
        }
        Dusuario dts = new Dusuario();
        fusuario func = new fusuario();

        dts.setUsuario(txtusuario.getText());
        dts.setContra(txtcontra.getText());
        int seleccionado = cbotipo.getSelectedIndex();
        dts.setTipo((String) cbotipo.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Usuario fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        } else if (accion.equals("editar")) {
            dts.setId(Integer.parseInt(txtid.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El usuario fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnguardarActionPerformed

