        int fila = tablaListaEnvio.rowAtPoint(evt.getPoint());

        String codigoEnvio= (tablaListaEnvio.getValueAt(fila, 0).toString());
        
         modeloTablaDetalle=objEnvios.obtenerDetalleEnvio(codigoEnvio);
         tablaListaDetalle.setModel(modeloTablaDetalle);
         lblEnvio.setText(codigoEnvio);

